# the (.) after the name in name.title() tells Python to make the title() method act on the variable "name".
# the title() method changes each word to title case, where each word begins with a capital letter.
name = "ada lovelace"
print(name.title())

# changes a string to all uppercase or all lowercase respectively
name = "Ada Lovelace"
print(name.upper())
print(name.lower())